		<h4><span class="dashicons dashicons-editor-help"></span> How to add an icon to menu?</h4>
		<ul>
			<li>Go to page Wow Icons</li>
			<li>Select icon fonts for menu and click "Save"</li>
			<li>Generate class of icon</li>
			<li>Go to Appearance -> Menus, select which menu item to which you want to add the icon, and add the icon class(es) under 'CSS Classes (optional)'</li>
			<li>Save your menu</li>
		</ul>
		<h4><span class="dashicons dashicons-editor-help"></span> Why don't I see an option to add classes?</h4>
		Under Appearance -> Menus, click 'Screen Options' (top right of screen) and make sure that 'CSS Classes' is checked. If not - check it!		
